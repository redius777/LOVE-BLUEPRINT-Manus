/** タイプコードの軸を日本語に変換する */
export function decodeTypeCode(code: string): string {
  if (code.length < 4) return code;
  const d = code[0] === "D" ? "主導型" : "受容型";
  const i = code[1] === "I" ? "激しさ志向" : "穏やか志向";
  const v = code[2] === "V" ? "視覚" : "感覚";
  const s = code[3] === "S" ? "定番" : "冒険";
  return `${d} / ${i} / ${v} / ${s}`;
}
